package javas.Basic;

public class ForLoop {
    public static void main(String[] arg){
        for(int i=0;i<=10;i++){
            System.out.println(i);
        }

        int[] arr = {1,3,4,2,4,35,43,55};

        for(int i=0;i<arr.length;i++){
            System.out.println(arr[i]);
        }
    }
}
