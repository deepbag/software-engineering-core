package javas.Basic;

public class VeriableDataTypes {
    public static void main(String[] arg){
        int iage = 20;
        double dage = 20.00;
        float fage = 20.0F;
        long lage = 20L;
        boolean isJava = true;
        String name = "Java";
        char aplha = 'J';

        int[] arr = {1, 2, 3, 4};
        double[] darr = {1.00,2.00};
        float[] farr = {1.00F,2.00F};
        long[] larr = {1L,2L};
        boolean[] barr = {true,false};
        String[] sarr = {"Deep","Java"};
        char[] carr = {'A','B','C'};

        System.out.println("Integer: "+ iage);
        System.out.println("Double: "+ dage);
        System.out.println("Float: "+ fage);
        System.out.println("Long: "+ lage);
        System.out.println("Boolean: "+ isJava);
        System.out.println("Character: "+ aplha);

        System.out.println("Integer of Array: "+ arr);
        System.out.println("Double of Array: "+ darr);
        System.out.println("Float of Array: "+ farr);
        System.out.println("Long of Array: "+ larr);
        System.out.println("Boolean of Array: "+ barr);
        System.out.println("String of Array: "+ sarr);
        System.out.println("Char of Array: "+ carr);
    }
}
