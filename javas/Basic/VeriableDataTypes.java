package javas.Basic;

public class VeriableDataTypes {
    public static void main(String[] arg){
        int iage = 20;
        double dage = 20.00;
        float fage = 20.0F;
        long lage = 20L;
        boolean isJava = true;
        String name = "Java";
        char aplha = 'J';

        System.out.println("Integer: "+ iage);
        System.out.println("Double: "+ dage);
        System.out.println("Float: "+ fage);
        System.out.println("Long: "+ lage);
        System.out.println("Boolean: "+ isJava);
        System.out.println("Character: "+ aplha);
    }
}
