package javas.Basic;

public class Switch {
    public static void main(String[] arg){
        int age = 2;

        switch (age){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("0");
        }
    }
}
